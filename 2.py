"""簡單示範類別：狗狗（Dog）

費曼式說明：
- 這個檔案示範怎麼定義一個『狗』的物件，讓程式可以知道牠的名字和品種，並讓牠會叫。
- 類比：把狗的名字和品種寫在一張卡片上，之後要牠叫就讀出來。
"""


class Dog:
    # 費曼式解釋（建構子）
    # 1) 每次我們建立一隻狗，會給牠一個名字和品種，這些資訊放在物件裡。
    # 2) 類比：建構子就像做一張新卡片，把名字和品種寫上去，這張卡片就是一個 Dog 物件。
    def __init__(self, name, breed):
        self.name = name
        self.breed = breed

    # 費曼式解釋（方法：叫聲）
    # 1) bark() 是狗會發出的聲音，這裡回傳一個格式化的字串。
    # 2) 類比：像你按一個按鈕，狗就會說出 "XXX says Woof!"。
    # 3) 重點：方法會使用物件裡存的名字和品種，拼成一句完整的話。
    def bark(self):
        return f"{self.name} the {self.breed} says Woof!"

# 程式的主流程（輸入、處理、輸出）
# 費曼式解釋：
# 1) 我們從第一行輸入讀取名字與品種，並建立 Dog 物件。
# 2) 類比：像在動物展覽上登記資訊，然後問牠 "會叫嗎？"，牠回答一段話。

# 讀取使用者輸入（例如："Buddy GoldenRetriever"）
name, breed = input().split()

# 建立一個狗狗物件，把剛剛讀到的名字和品種放進去
dog = Dog(name, breed)

# 叫出狗狗的叫聲並印出
print(dog.bark())
